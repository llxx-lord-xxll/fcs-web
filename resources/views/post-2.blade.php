                <p>Meet Zaki Yamani, a young leading serial entrepreneur and a family business owner basing in Jakarta, tapping in education and digital marketing industries. Zaki came to learn about Future City Summit after his first experience in Hong Kong with Asia Institute for Political Economy(AIPE).</p>

