                <p>
                    With the world’s fourth largest population and rising, Indonesia is in the midst of a housing crisis. Approximately 11.8 million Indonesians will never own their own home, according to the Indonesian Ministry of Economic Affairs, unless government construction targets are met.
                </p>
                <p>In response to a chronic need for homes, Indonesia’s government launched the One Million Homes programme in mid-2015. The goal of this initiative is to cut Indonesia’s housing needs considerably. By 2019, the government hopes to have slashed the nation’s housing backlog from 11.4 million homes to 6.9 million.</p>
                <p>Zaki has already been successfully running his few companies in Indonesia along with his family business in property. Yet, the impact of FCS has sowed a seed deep in heart for him to take leap of faith again and challenge the industry which he earns most. </p>
                <p>FCS2016 was the first year of Future City Summit with the support of Global Partnership Seed Fund for United Nations SDGs from Vice President Office of The University of Hong Kong, with the theme “Connecting Cities”.</p>
                <p>The panel and keynotes focus on architecture, urban planning and education for entrepreneurship movement, with speakers from well-known architect and urban planning advocates in Hong Kong, as well as Secretary of Environmental Bureau of Hong Kong SAR, <strong>Christine Loh Kung-wai,</strong> OBE, JP. </p>
                <p>The theme and insights in sessions and Metro-Hackathon which focuses on UN SDG #11 Sustainable Cities and Communities and #9 Industry, Innovation and Infrastructure have caught Zaki’s attention for a change in his country through business innovation.</p>

